            'TABINDEX' => strval($tabindex),
            'NAME_STUB' => $name,
            'I' => strval($i),
            'REQUIRED' => ($i >= $num_required) ? '' : '_required',
            'DEFAULT' => '',
            'PATTERN' => $pattern,
        )));
    }
    return _form_input(preg_replace('#\[\]$#', '', $name), $pretty_name, $description, $input, $num_required > 0, false, $tabindex, false, true, '', $pattern_error);
