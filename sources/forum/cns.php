    public function get_mrow($name)
    {
        foreach ($this->MEMBER_ROWS_CACHED as $i => $row) {
            if ($row['m_username'] == $name) {
                return $row;
            }
        }
        $rows = $this->connection->query_select('f_members', array('*'), array('m_username' => $name), '', 1);
        if (!array_key_exists(0, $rows)) {
            return null;
        }
        return $rows[0];
    }

