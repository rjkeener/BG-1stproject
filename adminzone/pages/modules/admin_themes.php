            $color = post_param_string('c' . strval($i), '');
        }

        // Convert from current path to save path
        $custom_path = get_custom_file_base() . '/themes/' . filter_naughty($theme) . '/css_custom/' . $file;

        // Store revision
        if (addon_installed('actionlog')) {
